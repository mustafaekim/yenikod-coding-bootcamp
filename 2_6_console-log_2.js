
// We already know that string literal represents a string value
// Learn also that, a variable represents the value inside the variable
// Hence, instead of writing a string literal, you can also use a variable that holds the same string


// Example 1:
var myName = "Mustafa";
console.log(myName);

/*
    Example 2:
    The code above produces the same output as the code below:
    console.log("Mustafa")

    Note that, 
        * in the first example we passed a string value into the console.log()
        * in the second example we passed a variable which represents a string value
        * They both produce the same result
*/