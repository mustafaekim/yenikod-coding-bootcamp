/*
    While operators perform some actions on the operand(s), they also return a value
*/

var x = 10;
console.log(x + 1);
console.log(2 * 3 - 1 / 5);


/*
    The assignment operator both
        assigns the value on the right into the variable on the left
        returns the final (after the assignment) value in the variable
*/

console.log(x = 20);