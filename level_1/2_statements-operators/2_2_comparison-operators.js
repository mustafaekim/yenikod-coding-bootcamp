/*
    Comparison operators
        <       less than
        >       greater than
        <=      less than or equal to
        >=      greater than or equal to

    Comparison operators return a value of type boolean.
    Hence, either true or false
*/

console.log(5<6);
console.log(7<6);

console.log(7>6);
console.log(5>6);

var x = 10;
console.log(x >= 10);
console.log(x <= 10);

console.log(x >= 10.1);