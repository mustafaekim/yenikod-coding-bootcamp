/*
    "if" statement can be extended with the else statement, which must follow the if block
    if the condition expression returns false, the else block is executed
*/

var x = 6;

if(x % 2 == 0){
    console.log(x + " is an even number");
}
else{
    console.log(x + "is an odd number");
}
console.log("End of program..");
